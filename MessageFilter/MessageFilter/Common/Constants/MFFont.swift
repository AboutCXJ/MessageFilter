import UIKit

class MFFont: UIFont {
    /// 平方-细体
    ///
    /// - Parameter size: 大小
    class func getFontW3(size: CGFloat) -> UIFont {
        return UIFont.systemFont(ofSize: size, weight: UIFont.Weight.light)
    }
    
